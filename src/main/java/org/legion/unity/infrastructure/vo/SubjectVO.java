package org.legion.unity.infrastructure.vo;

import org.legion.unity.common.base.BaseVO;
import org.legion.unity.infrastructure.entity.Subject;


public class SubjectVO extends BaseVO {

    public SubjectVO(Subject subject) {
        super(subject);
    }

    private String id;
    private String topId;
    private String subId;
    private String name;
    private String description;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTopId() {
        return topId;
    }

    public void setTopId(String topId) {
        this.topId = topId;
    }

    public String getSubId() {
        return subId;
    }

    public void setSubId(String subId) {
        this.subId = subId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
